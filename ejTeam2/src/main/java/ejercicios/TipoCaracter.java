package ejercicios;

// Un enum es básicamente un tipo que solo puede tomar un conjunto fijo de valores.
// En vez de usar números mágicos (0 = vocal, 1 = consonante, etc.) usamos nombres con significado.
// Eso hace que el código se lea casi como lenguaje natural.
public enum TipoCaracter {
    VOCAL,
    CONSONANTE,
    DIGITO,
    OTRO
}
